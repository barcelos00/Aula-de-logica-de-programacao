#include <iostream>
#include <stdio.h>
#include <stdlib.h>

using namespace std;
int main (){
int x = 3;
    int dobro, triplo, quadrado;
    dobro = x * 2;
    triplo = x * 3;
    quadrado = x * x;
    printf("Numero escolhido: %d\n", x);
    printf("Dobro: %d\n", dobro);
    printf("Triplo: %d\n", triplo);
    printf("Quadrado: %d\n", quadrado);

    return 0;

}
